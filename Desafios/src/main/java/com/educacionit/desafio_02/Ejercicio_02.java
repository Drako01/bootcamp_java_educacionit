package com.educacionit.desafio_02;

public class Ejercicio_02 {

	public static void main(String[] args) {
		int n1 = 5;
        int n2 = 10;
        int n3 = 20;

        
        int resultadoSuma = n1 + n2;
        int resultadoResta = n3 - n1;
        int resultadoMultiplicacion = n1 * n3;
        double resultadoDivision = (double) n3 / n2; 

        
        System.out.println("a) n1 + n2 = " + resultadoSuma);
        System.out.println("b) n3 - n1 = " + resultadoResta);
        System.out.println("c) n1 * n3 = " + resultadoMultiplicacion);
        System.out.println("d) n3 / n2 = " + resultadoDivision);

	}

}
